import { mkdir, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sessionsRoot = path.join(projectRoot, "content", "curriculum", "sessions");
const outputPath = path.join(projectRoot, "content", "curriculum", "session-index.ts");

async function collect(directory) {
  let entries;
  try {
    entries = await readdir(directory, { withFileTypes: true });
  } catch (error) {
    if (error.code === "ENOENT") return [];
    throw error;
  }

  const files = [];
  for (const entry of entries) {
    const target = path.join(directory, entry.name);
    if (entry.isDirectory()) files.push(...await collect(target));
    if (entry.isFile() && entry.name.endsWith(".ts")) files.push(target);
  }
  return files.sort((left, right) => left.localeCompare(right, "en"));
}

const files = await collect(sessionsRoot);
const imports = files.map((file, index) => {
  const relative = path.relative(path.dirname(outputPath), file).replaceAll("\\", "/");
  const specifier = relative.startsWith(".") ? relative : `./${relative}`;
  return `import session${index + 1} from ${JSON.stringify(specifier)};`;
});
const names = files.map((_, index) => `session${index + 1}`);

const source = `// Generated by scripts/generate-curriculum-index.mjs. Do not edit by hand.
import type { DetailedSession } from "./types";
${imports.join("\n")}

export const detailedSessions: DetailedSession[] = ([${names.join(", ")}] as DetailedSession[]).sort(
  (left, right) => left.courseId.localeCompare(right.courseId, "en")
    || left.moduleId.localeCompare(right.moduleId, "en")
    || left.order - right.order,
);

export const detailedSessionBySlug = new Map(
  detailedSessions.map((session) => [session.slug, session]),
);
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, source, "utf8");
console.log(`Generated curriculum index with ${files.length} sessions.`);
